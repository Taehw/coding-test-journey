public class boj_2741 {

    public static void main(String[] args) {

        //브5 - N찍기
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();

        for (int i = 1; i <= a; i++) {
            System.out.println(i);
        }
    }
}
